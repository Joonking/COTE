int main()
{
    ios::sync_with_stdio(false);
    cin.tie(nullptr);
    cout.tie(nullptr);
    
    int N;
    cin >> N;
    vector<int> A(N);
    
    for (int i = 0; i < N; i++)
    {
        cin >> A[i];
    }
    
    priority_queue<int> pq;
    
    for (int i : A)
    {
        if (pq.empty())
            pq.push(i);
        else
        {
            if (pq.top() <= i)
            {
                pq.push(i);
            }
        }
    }
    
    cout << pq.size() << endl;
    
    return 0;
}


